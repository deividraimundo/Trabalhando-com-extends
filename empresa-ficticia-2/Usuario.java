
public class Usuario {
    private String nome;
    private int senha;
    
    public String getNome(){
        return this.nome;
    }
    public void setNome(String nome){
        this.nome = nome;
    }
    
    public int getSenha(){
        return this.senha;
    }
    public void setSenha(int senha){
        this.senha = senha;
    }
}
